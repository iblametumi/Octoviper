const symbols = ['^DJI', '^GSPC', '^IXIC']; // Example symbols

const cardContent = document.querySelector('.card-content');

async function fetchAndDisplayData() {
  const baseUrl = 'https://api.polygon.io/v2/aggs/ticker/';
  const range = '1/day';

  try {
    for (const symbol of symbols) {
      const url = `${baseUrl}${symbol}/range/${range}?apiKey=${apiKey}`;
      const response = await fetch(url);
      const data = await response.json();

      if (data.status !== 'OK') {
        console.error('Error fetching data for', symbol, data);
        continue;
      }

      const price = data.results[0].c;
      const change = data.results[0].c - data.results[0].o;
      const changePercent = (change / data.results[0].o) * 100;

      // Update the card content
      cardContent.innerHTML += `
        <p><strong>${symbol}:</strong> $${price.toFixed(2)} ($${change.toFixed(2)} - ${changePercent.toFixed(2)}%)</p>
      `;
    }
  } catch (error) {
    console.error('Error fetching data:', error);
    cardContent.innerHTML = 'Error fetching data';
  }
}

// Call the function to fetch and display data
fetchAndDisplayData();